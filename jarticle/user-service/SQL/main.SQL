CREATE DATABASE  IF NOT EXISTS jarticle DEFAULT CHARSET utf8 COLLATE utf8_general_ci;

use jarticle;

DROP TABLE userInfo;

CREATE TABLE userInfo (
    userID INT AUTO_INCREMENT PRIMARY KEY  COMMENT 'id',
    userName VARCHAR(100) NOT NULL UNIQUE  COMMENT '用户名',
    password VARCHAR(100) NOT NULL COMMENT '密码',
    userIcon VARCHAR(300) NULL COMMENT '头像地址',
    role VARCHAR(300) NULL COMMENT '角色，逗号分割',
    enable INT NOT NULL DEFAULT(1) COMMENT '是否启用，默认为1；0=禁用，1=启用',
    createdBy VARCHAR(300) NULL COMMENT '操作人',
    createdDate timestamp NULL default CURRENT_TIMESTAMP COMMENT '创建日期'
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT '用户信息表';

